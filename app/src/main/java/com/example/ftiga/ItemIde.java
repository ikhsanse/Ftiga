package com.example.ftiga;

public class ItemIde {
    private String id;
    private String judul_ide;
    private String dana;
    private String nama;
    private String deskripsi;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getJudul_ide() {
        return judul_ide;
    }

    public void setJudul_ide(String judul_ide) {
        this.judul_ide = judul_ide;
    }

    public String getDana() {
        return dana;
    }

    public void setDana(String dana) {
        this.dana = dana;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }
}
